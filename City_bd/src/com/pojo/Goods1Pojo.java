package com.pojo;

public class Goods1Pojo {
	private int id;
	private String name;
	private int isDelete;
	public Goods1Pojo(){}
	public Goods1Pojo(int id, String name, int isDelete) {
		super();
		this.id = id;
		this.name = name;
		this.isDelete = isDelete;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getIsDelete() {
		return isDelete;
	}
	public void setIsDelete(int isDelete) {
		this.isDelete = isDelete;
	}
}
